<p align="left">Phương pháp trắc nghiệm: có ba loại</p>

<p align="left">Trắc nghiệm theo nghĩa rộng là một hoạt động được thực hiện để đo lường năng lực của các đối tượng nào đó nhằm những mục đích xác định.</p>

<p align="left">   Trong giáo dục, trắc nghiệm được tiến hành thường xuyên ở các kỳ thi, kiểm tra để đánh giá kết quả học tập, giảng dạy đối với một phần của môn học, toàn bộ môn học, đối với cả một cấp học; hoặc để tuyển chọn một số người có năng lực nhất vào học một khoá học.</p>

<p align="left">  Có thể phân chia các phương pháp trắc nghiệm ra làm 3 loại: loại quan sát, loại vấn đáp và loại viết.</p>

<p align="left">   1. Loại quan sát giúp xác định những thái độ, những phản ứng vô ý thức, những kỹ năng thực hành và một số kỹ năng về nhận thức, chẳng hạn cách giải quyết vấn đề trong một tình huống đang được nghiên cứu.</p>

<p align="left">  2.Loại vấn đáp có tác dụng tốt khi nêu các câu hỏi phát sinh trong một tình huống cần kiểm tra. Trắc nghiệm vấn đáp thường được dùng khi tương tác giữa người chấm và người học là quan trọng, chẳng hạn cần xác định thái độ phản ứng khi phỏng vấn…</p>

<p align="left">   3. Loại viết thường được sử dụng nhiều nhất vì nó có những ưu điểm sau:</p>

<p align="left">  - Cho phép kiểm tra nhiều thí sinh một lúc.</p>

<p align="left"> - Cho phép thí sinh cân nhắc nhiều hơn khi trả lời.</p>

<p align="left"> - Đánh giá được một vài loại tư duy ở mức độ cao.</p>

<p align="left"> - Cung cấp bản ghi rõ ràng các câu trả lời của thí sinh để dùng khi chấm.</p>

<p align="left"> - Dễ quản lý hơn vì bản thân người chấm không tham gia vào bối cảnh kiểm tra.</p>

<p align="left"> Trắc nghiệm viết được chia thành 2 nhóm chính:</p>

<p align="left">Nhóm các câu hỏi trắc nghiệm buộc trả lời theo dạng mở, thí sinh phải tự trình bày ý kiến trong một bài viết dài để giải quyết vấn đề mà câu hỏi nêu ra. Người ta gọi trắc nghiệm theo kiểu này là kiểu tự luận (essay). Phương pháp tự luận rất quen biết với mọi người chúng ta.</p>

<p align="left">  Nhóm các câu trắc nghiệm mà trong đó đề thi thường gồm rất nhiều câu hỏi, mỗi câu nêu ra một vấn đề cùng với những thông tin cần thiết sao cho thí sinh chỉ phải trả lời vắn tắt cho từng câu. Người ta thường gọi nhóm phương pháp này là trắc nghiệm khách quan (objective test).</p>

<p align="left"> Nhiều người thường gọi tắt phương pháp trắc nghiệm khách quan là trắc nghiệm. Thuận theo thói quen ấy, từ nay về sau khi dùng từ “trắc nghiệm” mà không nói gì thêm thì chúng ta sẽ ngầm hiểu là nói đến trắc nghiệm khách quan.</p>

